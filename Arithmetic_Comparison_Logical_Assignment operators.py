# Arithmetic Operators

# add
print(1 + 2)
# multiply
print(1 * 2)
# divide
print(1 / 2)
# remainder
print(1 % 2)
# subtract
print(1 - 2)
# power
print(2 ** 5)

# Comparison Operators

# > , < , >= , <= , == , !=
print(9 > 8)
print(9 < 8)
print(9 >= 9)
print(9 <= 9)
print(10 == 10)
print(10 != 10)

# Logical Operators

# and , or

print((10 > 5) and (10 < 5))
print((10 > 5) and (10 > 5))
print((10 > 5) and (10 > 5) and "a" == "a")

print((10 > 5) or (10 < 5))
print((10 > 5) or (10 > 5))
print((10 > 5) or (10 > 5) and "a" == "a")

# you can combine and and or operators in a single statement

print(not ("Shyam" == "Sonu"))

